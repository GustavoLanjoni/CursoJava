package cursojava.executavel;

public class ArrayVetores {

	public static void main(String[] args) {
		
		//Array pode ser todos tipos  de dados e objetos também
		
		//Array sempre dever ter a quantdade de posições definida
		double notas[] = new double[4];
		
		//Atribuir valor nas posições do arrays 
		notas[0] = 9.8;
		notas[1] = 4.0;
		notas[2] = 1.1;
		notas[3] = 0.0;
		
				
		
		System.out.println("nota 0 = " + notas[0]);
		System.out.println("nota 1 = " + notas[1]);
		System.out.println("nota 2 = " + notas[2]);
		System.out.println("nota 3 = " + notas[3]);
		
	}
	
}
